inline ll gcd(ll a, ll b) { return b == 0 ? a : gcd(b, a % b); }
inline ll lcm(ll a, ll b) { return a / gcd(a, b) * b; }

/**
 * Check a number is prime
 *
 * input: n (0 <= n <= 10^9)
 * output: YES or NO
 */

int is_prime(ll n) {
    for (ll i = 2; i * i <= n; i++) {
        if (n % i == 0)
            return 0;
    }

    return n > 1;
}

